// Helper to create assignee with team_id (matching actual API structure)
function createAssignee(
  userId: string,
  userName: string,
  taskId: string,
  teamId: string | null,
  isActive: boolean = true
): TodoAPI.Assignee {
  return {
    id: `assignee-${taskId}`,
    task_id: taskId,
    assignee_id: userId,
    assignee_name: userName,
    user_type: 'user',
    executor_id: null,
    team_id: teamId,
    is_active: isActive,
    created_by: userId,
    updated_by: null,
    created_at: new Date().toISOString(),
    updated_at: null,
  };
}

